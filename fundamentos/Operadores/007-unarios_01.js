let a = 1;

//operadores binários
// o '+' é um operador binário quando está entre os operandos (seja variaveis ou literais) é chamado infix
a = a + 1;
a += 1;

// operador unário
// o '++' (ou '--') é um operador de incremento, fica após o operando, é chamado postfix
a ++ ; 
++a;
// o '++' (ou '--') é um operador de incremento, fica antes o operando, é chamado pretfix
a--;
--a;

console.log(a);